#include "basicMath.h"

int add(int num1, int num2) {
    return num1 + num2;
}

int reduce(int num1, int num2) {
    return num1 - num2;
}

int multi(int num1, int num2) {
    return num1 * num2;
}

int self_div(int num1, int num2) {
    return num1 / num2;
}

